package com.dcardprocessing.model;

public class EndTimeDetail {
	private Long freelancer_id;
	private String token;
	private int time_id;
	private int project_lead_id;
	/**
	 * @return the freelancer_id
	 */
	public Long getFreelancer_id() {
		return freelancer_id;
	}
	/**
	 * @param freelancer_id the freelancer_id to set
	 */
	public void setFreelancer_id(Long freelancer_id) {
		this.freelancer_id = freelancer_id;
	}
	/**
	 * @return the token
	 */
	public String getToken() {
		return token;
	}
	/**
	 * @param token the token to set
	 */
	public void setToken(String token) {
		this.token = token;
	}
	/**
	 * @return the time_id
	 */
	public int getTime_id() {
		return time_id;
	}
	/**
	 * @param time_id the time_id to set
	 */
	public void setTime_id(int time_id) {
		this.time_id = time_id;
	}
	/**
	 * @return the project_lead_id
	 */
	public int getProject_lead_id() {
		return project_lead_id;
	}
	/**
	 * @param project_lead_id the project_lead_id to set
	 */
	public void setProject_lead_id(int project_lead_id) {
		this.project_lead_id = project_lead_id;
	}
	
	
}
